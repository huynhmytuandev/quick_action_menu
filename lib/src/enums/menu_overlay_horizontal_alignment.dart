/// Defines the horizontal alignment strategy for overlay menus.
///
/// This enum determines how the menu content is aligned relative to
/// the anchor widget horizontally.
enum OverlayMenuHorizontalAlignment {
  /// Align the menu to the left edge of the anchor.
  left,

  /// Center the menu horizontally relative to the anchor.
  center,

  /// Align the menu to the right edge of the anchor.
  right,
}
